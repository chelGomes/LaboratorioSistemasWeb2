package br.ufjf.dcc193.Michel.atividade4;

public class Detalhe_pedido_pizza {
    private String nome;
    private String endereco;
    private String saborPizza;
    private String tamanhoPizza;
    private String refrigerante;
    private String formaPagamento;

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getEndereco() {
        return endereco;
    }
    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }
    public String getSaborPizza() {
        return saborPizza;
    }
    public void setSaborPizza(String saborPizza) {
        this.saborPizza = saborPizza;
    }
    public String getTamanhoPizza() {
        return tamanhoPizza;
    }
    public void setTamanhoPizza(String tamanhoPizza) {
        this.tamanhoPizza = tamanhoPizza;
    }
    public String getRefrigerante() {
        return refrigerante;
    }
    public void setRefrigerante(String refrigerante) {
        this.refrigerante = refrigerante;
    }
    public String getFormaPagamento() {
        return formaPagamento;
    }
    public void setFormaPagamento(String formaPagamento) {
        this.formaPagamento = formaPagamento;
    }

    
}
